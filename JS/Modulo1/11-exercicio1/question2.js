const x = parseFloat(prompt("Enter the first number:"));
const y = parseFloat(prompt("Enter the second number:"));

const addition = x + y;
const subtraction = x - y;
const multiplication = x * y;
const division = x / y;

alert(
  "Addition: " +
    addition +
    "\nSubtraction: " +
    subtraction +
    "\nMultiplication: " +
    multiplication +
    "\nDivision: " +
    division
);
